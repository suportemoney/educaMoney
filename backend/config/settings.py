def _resolver_app_version() -> str:
    """Versão automática do build (/etc/educamoney/VERSION) ou override APP_VERSION."""
    env_ver = os.environ.get("APP_VERSION", "").strip()
    if env_ver:
        return env_ver
    for caminho in (
        Path("/etc/educamoney/VERSION"),
        BASE_DIR / "VERSION",
    ):
        try:
            texto = caminho.read_text(encoding="utf-8").strip()
            if texto:
                return texto
        except OSError:
            continue
    return "0-unknown"


APP_VERSION = _resolver_app_version()
